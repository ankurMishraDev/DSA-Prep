package Strings;

public class prettyPrinting {
    public static void main(String[] args) {
        float variable = 346.9837516f;
        System.out.printf("Formatted number is : %.3f " , variable);

        System.out.printf("\n Value of Pie until 0.3 decimal: %f", Math.PI);

        System.out.printf("\n Hello my name is %s and I am a %s", "Ankur", "Professional Footballer");
    }
}
